#Faça um algoritmo que leia o salário de um funcionário e mostre seu novo salário, com 15% de aumento. 
print('\nSeja bem-vindo, lhe estarei ajudando a saber seu novo salário!')
salario = float(input('Me diga qual o seu salário: R$ '))
aumento = salario * 15 /100
novo_salario = salario + aumento

print('\nEita como é bom salário na conta!')
print(f'Seu novo salário com 15% de aumento é: R${novo_salario}')
print('Espero lhe tenha ajudado, até mais!')

