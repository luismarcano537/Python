#Escreva um programa que converta uma temperatura digitando em graus celsius e converta para graus fahrenheit.
c = float(input('\nDigite a temperatura em Gº que lhe darei em Fº: '))
f = ((c * 9) / 5 + 32)

print(f'A temperatura Cº{c}, em Fahrenheit é: Fº{f}')
print('Obrigado por ter utilizado nosso programa!')

